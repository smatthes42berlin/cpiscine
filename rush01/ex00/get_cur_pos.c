void	get_cur_pos(int **board, int *pos, int n)
{
	int	i;
	int	j;

	i = -1;
	j = -1;
	while (++i < n)
	{
		while (++j < n)
		{
			if (board[i][j] == 0)
			{
				pos[0] = i;
				pos[1] = j;
				return ;
			}
		}
		j = -1;
	}
	return ;
}
